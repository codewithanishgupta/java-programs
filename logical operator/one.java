// Write a Java program to demonstrate bitwise AND (&) operator.

public class one {
    public static void main(String[] args) {
        int x=12;   // binary=1100
        int y=26;   // binary=11001
        int result=x&y; // binary= 1100 & 11001 =   1000    =8
        System.out.println(result);
    }
}
